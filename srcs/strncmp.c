#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0' ) && i < n)
	{
		if ((int) s1[i] != (int) s2[i])
		{
			return ((int) s1[i] - (int) s2[i]);
		}
		i++;
	}
	return (0);
}
