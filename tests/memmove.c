#include "libft.h"

void	*ft_memmove(void *destination, const void *source, size_t n)
{
	size_t					i;

	if (!destination && !source)
		return (NULL);
	if (destination > source)
	{
		i = n - 1;
		while (i >= 0)
		{
			((char *)dest)[i] = ((char *)src)[i];
			i--;
		}
	}
	else
	{
		i = 0;
		while (i <= n)
		{
			((char *)dest)[i] = ((char *)src)[i];
			i++;
		}
	}
	return (destination);
}
