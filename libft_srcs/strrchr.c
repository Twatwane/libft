#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	size_t	len;

	len = ft_strlen(str) + 1;
	while (len--, len + 1 >= 1)
		if ((unsigned char) str[len] == (unsigned char) c)
			return ((char *) &str[len]);
	return (NULL);
}
